document.getElementById("index-btn").addEventListener("click", function(){
    let hidenUl = document.getElementsByClassName("index-hiden-ul")[0]
    if (hidenUl.style.display === "none"){
        hidenUl.style.display = "block"
    } else {
        hidenUl.style.display ="none"
    }
})


